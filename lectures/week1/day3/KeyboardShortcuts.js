// Keyboard shortcuts

// General purpose shortcuts

// Save files
// Windows: ctrl-s
// Mac: cmd-s

// Copy files
// Windows: ctrl-c
// Mac: cmd-c

// Paste files
// Windows: ctrl-v
// Mac: cmd-v

// Cut files
// Windows: ctrl-x
// Mac: cmd-x

// Switch windows / switch between different programs
// Windows: alt-tab
// Mac: cmd-tab

// Reverse Switch windows / reverse switch between different programs
// Windows: alt-shift-tab
// Mac: cmd-shift-tab

// Select all
// Windows: ctrl-a
// Mac: cmd-a

// Undo button
// Windows: ctrl-z
// Mac: cmd-z

// Redo button
// Windows: ctrl-y
// Mac: cmd-shift-z

// Select multiple things
// Windows & Mac: shift-up or shift-down or shift-left or shift-right

// Searching for / finding simlar text/files
// Windows: ctrl-f
// Mac: cmd-f

// VS code specifc shortcuts

// Comment out a line(s) of code
// Windows: ctrl-/
// Mac: cmd-/

// Move a line of code up or down (note: you do not need to highlight the whole line)
// Windows: alt-up or alt-down
// Mac: opt-up or opt-down

// Copy a line of code up for down (note: you do not need to highlight the whole line)
// Windows: alt-shift-up or alt-shift-down
// Mac: opt-shit-up or opt-shift-down

// Create multiple cursors
// Windows: alt-click
// Mac: opt-click

// Selecting multiple same words (or highlighted portion)
// Windows: ctrl-d
// Mac: cmd-d

// Indenting lines of code
// Windows + Mac: tab

// UnIndenting lines of code
// Windows + Mac: shift-tab

// Autoformat document
// Windows: shift-alt-f
// Mac: shift-opt-f

// Open the VS Code command pallete
// Windows: ctrl-shift-p
// Mac: cmd-shift-p

